What I have done / do is develop use case specifications and focus on the alternate path ie if a simple application posted one name value pair or 3 int, the alternate path would describe what would happen with all other input. The same can be used with many types of use cases. However where this fails is in someone defining the way say a password reset application should work in the first place. People find it hard to describe the use cases without an overview. This leads to something I have wanted to do for a while, that is create a set of UML models for software security. I'll start that as a project later this year ;-)

